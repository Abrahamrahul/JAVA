import java.util.*;
import java.io.*;
class Solution 
{
	public static void main(String []args)
	{
		for (int i = 0; i < 5; i++)
		{
           		for (int j = 4; j > 1; j--) 
			{
                		System.out.print(" "); 
            			for (int k = 0; k <= i; k++) 
				{
                			System.out.print("* "); 
 				}
            			System.out.println(); 
           
            		}

		}
	}
}

		
		
  		
        